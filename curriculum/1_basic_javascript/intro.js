/**
 * JavaScript ?
 * JavaScript (JS) is a lightweight, interpreted, or just-in-time compiled 
 * programming language with first-class functions. 
 * 
 * While it is most well-known as the scripting language for Web pages, 
 * many non-browser environments also use it, such as Node.js, 
 * Apache CouchDB and Adobe Acrobat. JavaScript is a prototype-based,
 *  multi-paradigm, single-threaded, dynamic language, supporting object-oriented, 
 * imperative, and declarative (e.g. functional programming) styles. 
 * Read more about JavaScript.
 * 
 * The standards for JavaScript are the ECMAScript Language Specification (ECMA-262) 
 * and the ECMAScript Internationalization API specification (ECMA-402).
 * As soon as one browser implements a feature, we try to document it.
 * This means that cases where some proposals for new ECMAScript features 
 * have already been implemented in browsers, documentation and examples in 
 * MDN articles may use some of those new features. Most of the time, 
 * this happens between the stages 3 and 4, and is usually before the spec is officially published.
 * 
 * Designed by	Brendan Eich of Netscape initially; others have
 * also contributed to the ECMAScript standard
 * 
 * First appeared	December 4, 1995; 26 years ago
 * 
 * Major implementations V8, JavaScriptCore, SpiderMonkey, Chakra
 * 
 * Typing discipline	Dynamic, weak, duck
 * Stable release ECMAScript 2021[2] Edit this on Wikidata / June 2021; 15 months ago
 */